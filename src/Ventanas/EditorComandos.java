    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jTextArea2.setText("");
        try {
            String sentencia = jTextArea1.getText().trim();
            Object objeto = BaseDatos.ejecutar(sentencia);
            if(objeto!=null){             
                if (objeto.getClass()==JDBC4ResultSet.class){
                    
                    rs =(ResultSet)objeto;
                    ResultSetMetaData mt = rs.getMetaData(); 
                    
                    for (int i = 1; i < mt.getColumnCount()+1; i++) {
                            jTextArea2.append("|  "+mt.getColumnName(i)+"  |");
                        }
                    jTextArea2.append("\n");  
                    
                    while(rs.next()) {
                        for (int i = 1; i < mt.getColumnCount()+1; i++) {
                            jTextArea2.append("|  "+rs.getString(i)+"  |");
                        }
                        jTextArea2.append("\n");
                    }
                }else{
                    if((int)objeto>0){
                        jTextArea2.setText("Sentencia ejecutada correctamente, se modificaron "+(int)objeto+" filas.");                        
                        Main.actualizarList();
                        for (int i = 0; i < Main.modelosTablas.length; i++) {
                            AbstractTableModel tb = Main.modelosTablas[i];
                            if (sentencia.toUpperCase().contains("DELETE")){                                
                                tb.fireTableRowsDeleted(0, tb.getRowCount()-1);
                            }
                            if (sentencia.toUpperCase().contains("UPDATE")){
                                tb.fireTableRowsUpdated(0, tb.getRowCount()-1);
                            }
                            if (sentencia.toUpperCase().contains("INSERT")){                                
                                tb.fireTableRowsInserted(0, tb.getRowCount()-1);
                                System.out.println("error");
                            }                            
                        }
                    }else{
                        jTextArea2.setText("Error al ejecutar la sentencia:\n\n"+sentencia);                
                    }
                }
            }else{
                jTextArea2.setText("Error al ejecutar la sentencia: "+sentencia);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "No se ha podido ejecutar la consulta.", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Map parameters = new HashMap();
            parameters.put("asd", "21");
            JasperReport jasperReport =
                    JasperCompileManager.compileReport(
                    "JavaR/Lista.jrxml");
            JasperPrint jasperPrint = JasperFillManager.fillReport(
                    jasperReport, parameters, new JRResultSetDataSource(rs));
            JasperViewer.viewReport(jasperPrint);
        } catch (JRException ex) {
            ex.printStackTrace();            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

